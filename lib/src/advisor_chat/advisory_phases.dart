/// Enum to represent the different phases of the advisory chat.
/// [introduction] is the phase where the user is introduced to the advisor and the purpose of the chat.
/// [questions] is the phase where the user is asked questions to understand their interests, values, and capabilities.
/// [surveys] is the phase where the user is given a survey to drill into more specific interests.
/// [suggestions] is the phase where the user is given suggestions based on the information provided.
/// [paths] is the phase where the user is given information about different career paths.
enum AdvisoryPhase { introduction, questions, surveys, suggestions, paths }
